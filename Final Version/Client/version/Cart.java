    private void PurchaseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_PurchaseMouseClicked
        try {
            int valid=Logic.functions.Purchase(ClientVersion.b.getItems(),TOTAL.getText());
            if(valid==1)
            {
                Vector temp=Logic.functions.getInfo(ClientVersion.b.getEmail());
                ClientVersion.b.setWallet(Integer.valueOf(temp.get(4).toString()));
                
                GmailAPI.sendMail(ClientVersion.b.getEmail(),"ORDER CONFIRMATION","Thanks for shopping with us!"+"\n"+Logic.functions.Reciept());
                new CheckOut_Form().setVisible(true);
                
                this.dispose();
                

                
            }
                            else if(valid==-1)
                    JOptionPane.showMessageDialog(this, "Insuffiecient balance");
            else
            {
                Logic.functions.fill_Cart_table(jTable1, ClientVersion.b.getItems(), TOTAL);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Cart.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Cart.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cart.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(Cart.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_PurchaseMouseClicked

