    private void ADDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ADDMouseClicked
        if(QTY.getText().isEmpty())
        {
            QTY.setText("0");
        }
        int max=Logic.functions.getMaxAmountItem(jTable1, ClientVersion.b.getItems());
        int qty=(Integer.valueOf(QTY.getText()));
        if(qty+1<=max)
        {
            QTY.setText( String.valueOf((Integer.valueOf(QTY.getText())+1)));
            REMOVE1.setBackground(jPanel6.getBackground());
        }
        else
        {
            ADD.setBackground(Color.white);
        }
    }//GEN-LAST:event_ADDMouseClicked

    private void REMOVE1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_REMOVE1MouseClicked
        if(QTY.getText().isEmpty())
        {
            QTY.setText("0");
        }
        if(Integer.valueOf(QTY.getText())>0)
        {
            QTY.setText( String.valueOf((Integer.valueOf(QTY.getText())-1)));
            REMOVE1.setBackground(jPanel6.getBackground());
            ADD.setBackground(jPanel6.getBackground());
            if(Integer.valueOf(QTY.getText())==0)
            {
                REMOVE1.setBackground(Color.white);
                
            }
        }
        else
        {
            REMOVE1.setBackground(Color.white);
        }
    }//GEN-LAST:event_REMOVE1MouseClicked

